#!/usr/bin/env node
// Zero-dependency Umi design-token generator.
//
// Reads the DTCG-subset JSON source (tokens/*.json) and emits the artifacts the
// two frontends consume as COMMITTED files. This matters: each frontend deploys
// as its own Vercel project with an app-scoped `npm install` + `npm run build`
// (vite/next), NEVER a root `pnpm`/`turbo` build — so the monorepo generator does
// not run at deploy time and dist/ must be checked into git. A CI job
// (.github/workflows/tokens-ci.yml) rebuilds and `git diff --exit-code`s dist/ to
// keep the committed output honest.
//
// Outputs:
//   dist/dashboard.css — :root custom properties for umi-dashboard (Vite @import via the @umi/tokens alias)
//   dist/landing.cjs   — Tailwind theme.extend object for umi-landing-page (require in tailwind.config.js)
//   dist/landing.mjs   — ESM mirror of landing.cjs
//   dist/tokens.json   — fully-resolved flat token set (documentation / future tooling)

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url)); // packages/tokens/build
const PKG = join(HERE, '..');
const SRC = join(PKG, 'tokens');
const DIST = join(PKG, 'dist');

const read = (f) => JSON.parse(readFileSync(join(SRC, f), 'utf8'));
const core = read('core.json');
const dashboard = read('dashboard.json');
const landing = read('landing.json');

const isLeaf = (n) => n && typeof n === 'object' && '$value' in n;

// Flatten core into a dotted lookup so {color.navy} references resolve.
const coreFlat = {};
(function walk(node, path) {
  if (isLeaf(node)) {
    coreFlat[path.join('.')] = node.$value;
    return;
  }
  for (const [k, v] of Object.entries(node)) if (!k.startsWith('$')) walk(v, [...path, k]);
})(core, []);

// Resolve a DTCG {group.token} alias to a concrete value; pass literals through.
function resolve(value) {
  if (Array.isArray(value)) return value.map(resolve);
  if (typeof value !== 'string') return value;
  const m = value.match(/^\{([^}]+)\}$/);
  if (!m) return value;
  if (!(m[1] in coreFlat)) throw new Error(`Unresolved token reference {${m[1]}}`);
  return coreFlat[m[1]];
}

// dashboard.css — one --var: value per token, groups become section comments.
function emitDashboardCss() {
  const lines = [
    '/* GENERATED by packages/tokens/build/build.mjs — do not edit by hand.',
    '   Source: packages/tokens/tokens/{core,dashboard}.json */',
    ':root {',
  ];
  for (const [group, tokens] of Object.entries(dashboard)) {
    if (group.startsWith('$')) continue;
    lines.push(`  /* ${tokens.$description || group} */`);
    for (const [name, tok] of Object.entries(tokens)) {
      if (name.startsWith('$')) continue;
      lines.push(`  --${name}: ${resolve(tok.$value)};`);
    }
  }
  lines.push('}', '');
  return lines.join('\n');
}

// Strip DTCG metadata + resolve refs, preserving group nesting → a plain object.
function plain(node) {
  if (isLeaf(node)) return resolve(node.$value);
  const out = {};
  for (const [k, v] of Object.entries(node)) if (!k.startsWith('$')) out[k] = plain(v);
  return out;
}

// landing theme object shaped for Tailwind's theme.extend.
function emitLandingTheme() {
  return {
    colors: plain(landing.colors),
    fontFamily: plain(landing.fontFamily),
    letterSpacing: plain(landing.letterSpacing),
  };
}

// Fully-resolved flat token set (file.group.token → value) for docs/tooling.
function emitFlat() {
  const out = {};
  for (const [file, data] of [['core', core], ['dashboard', dashboard], ['landing', landing]]) {
    (function walk(node, path) {
      if (isLeaf(node)) {
        out[[file, ...path].join('.')] = resolve(node.$value);
        return;
      }
      for (const [k, v] of Object.entries(node)) if (!k.startsWith('$')) walk(v, [...path, k]);
    })(data, []);
  }
  return out;
}

mkdirSync(DIST, { recursive: true });
const banner = '// GENERATED by packages/tokens/build/build.mjs — do not edit by hand.';
const theme = emitLandingTheme();

writeFileSync(join(DIST, 'dashboard.css'), emitDashboardCss());
writeFileSync(join(DIST, 'landing.cjs'), `${banner}\nmodule.exports = ${JSON.stringify(theme, null, 2)};\n`);
writeFileSync(
  join(DIST, 'landing.mjs'),
  `${banner}\n` +
    `export const colors = ${JSON.stringify(theme.colors, null, 2)};\n` +
    `export const fontFamily = ${JSON.stringify(theme.fontFamily, null, 2)};\n` +
    `export const letterSpacing = ${JSON.stringify(theme.letterSpacing, null, 2)};\n` +
    'export default { colors, fontFamily, letterSpacing };\n',
);
writeFileSync(join(DIST, 'tokens.json'), `${JSON.stringify(emitFlat(), null, 2)}\n`);

console.log('✓ packages/tokens: wrote dist/{dashboard.css,landing.cjs,landing.mjs,tokens.json}');
